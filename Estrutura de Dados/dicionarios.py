# {key: value}
"""
No dicionário é possível acessar qualquer elementos através da chave indicada -> chave-valor (key-value),
diferente de listas e tuplas que é possíve acessar elementos apenas atráves de números inteiros
"""

notas = {
    "alice": 10,
    "bob": 8,
    "carlos": 9
}
print(notas["alice"])

janeiro = {
    1: "Sábado",
    2: "Domingo"
}
print(janeiro[1])